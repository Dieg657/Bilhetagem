    private void btnAcaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAcaoActionPerformed
        empresa = new Empresa(txtCNPJ.getText());
        empresa.setFantasiaEmp(jComboBox1.getSelectedItem().toString());
        try {
            empresa = slctDAO.getEmpresa(empresa);
        } catch (SQLException ex) {
            Logger.getLogger(formSelecionaEmpresa.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(empresa == null){
            JOptionPane.showMessageDialog(this,"NÃ£o foram encontrados registros com esses dados!", "Erro",JOptionPane.ERROR_MESSAGE );
        }else{
            formFuncionario frmFuncionario = new formFuncionario(empresa.getCnpj());
            frmFuncionario.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_btnAcaoActionPerformed

